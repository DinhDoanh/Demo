import Foundation
import UIKit

extension UIColor {
    class func mainColor() -> UIColor {
        return UIColor(red: 0.50, green: 0.00, blue: 1.00, alpha: 1.00)
    }
    
    class func mainColorOpacity3() -> UIColor {
        return UIColor(red: 0.50, green: 0.00, blue: 1.00, alpha: 0.3)
    }
}
